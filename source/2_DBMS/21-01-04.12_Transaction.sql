-- [XII] 트랜잭션 명령어 - COMMIT; ROLLBACK; SAVEPOINT;
COMMIT; 
SELECT * FROM DEPT01;
DROP TABLE DEPT01;
CREATE TABLE DEPT01 AS SELECT * FROM DEPT;
SELECT * FROM DEPT01; --10, 20, 30, 40
DELETE FROM DEPT01; -- 전체 데이터 삭제(데이터 없음)
ROLLBACK; 
SELECT * FROM DEPT01; --10, 20, 30, 40
-- 새로운 트랜잭션 시작 --
DELETE FROM DEPT01 WHERE DEPTNO=40; --40번 부서 삭제
SELECT * FROM DEPT01; --10, 20, 30
COMMIT;
-- 새로운 트랜잭션 시작 --
DELETE FROM DEPT01 WHERE DEPTNO=30; --30번 부서 삭제
SELECT * FROM DEPT01; --10, 20
SAVEPOINT C1; --C1지점: 10, 20번 부서 有
DELETE FROM DEPT01 WHERE DEPTNO=20; --20번 부서 삭제
SELECT * FROM DEPT01; --10
SAVEPOINT C2; --C2지점: 10번 부서 有
DELETE FROM DEPT01 WHERE DEPTNO=10; --10번 부서 삭제
SELECT * FROM DEPT01; -- 부서 없음
ROLLBACK TO C2; --C2지점으로 -- 10번 부서 有
ROLLBACK TO C1; --C1지점으로 -- 10,20번 부서 有
ROLLBACK; --트랜잭션 단위 취소(10,20,30번 부서 有)